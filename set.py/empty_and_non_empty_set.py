# To create an empty set, use the function set()

names = set()
print(names)
print()
list_of_delicious_fruits = set()
print(list_of_delicious_fruits)
print()

# Non-empty sets
fruits = {'strawberry', 'kiwi', 'apple', 'mango'}
list_of_delicious_fruits = set(fruits)
print(list_of_delicious_fruits)
print(type(fruits))
print()

myset = {"apple", "banana", "cherry"}
print(myset)
print(type(myset))
print()

names = {'sylvia'}
print(names)